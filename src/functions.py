# region File Operations
from shutil import copytree, rmtree
def copy_tree(src, dst, deleteDstFirst=False):
    """
    Copies the contents of the source directory to the destination directory.

    Parameters:
        src (str): The path to the source directory.
        dst (str): The path to the destination directory.
        deleteDstFirst (bool): If True, deletes the destination directory before copying.
                               Defaults to False.

    Raises:
        FileNotFoundError: If the source directory does not exist.
    """

    if deleteDstFirst:
        try:
            rmtree(dst)
        except FileNotFoundError:
            pass
        except PermissionError as e:
            print(f"Permission error while deleting {dst}: {e}")
        except Exception as e:
            print(f"An error occurred while deleting {dst}: {e}")
        except FileNotFoundError:
            pass
    copytree(
        src,
        dst,
        dirs_exist_ok=not deleteDstFirst
    )
# endregion

# region Generate Page
def extract_title(markdown):
    """
    Extracts the title from the markdown content.

    Parameters:
        markdown (str): The markdown content.

    Returns:
        str: The extracted title.
    """
    blocks = markdown_to_blocks(markdown)
    for block in blocks:
        if block.startswith("# "):
            return block[2:]
    return None

def generate_page(from_path, template_path, dest_path):
    print(f"Generating page from {from_path} to {dest_path} using {template_path}")
    title = extract_title(open(from_path).read())
    content = open(from_path).read()
    htmlcode = markdown_to_html_node(content)
    with open(template_path) as template_file:
        template = template_file.read()
    page = template.replace("{{ Title }}", title).replace("{{ Content }}", htmlcode.to_html())
    with open(dest_path, "w") as dest_file:
        dest_file.write(page)
# endregion
